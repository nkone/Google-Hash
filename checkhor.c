#include "hashcode.h"

int		checkhor(char **s)
{
	int		x;
	int		y;

	x = 1;
	y = 0;
	while (s[x])
	{
		if (s[x][y] == 'H')
			return (1);
		else
			return (0);
		x++;
	}
	return (0);
}
